package EjerciciosGuia;
public class Clasemath{
    //Uso de algunos metodos de la clase Math
    public static void main(String []args){
        // metodo pi
        System.out.println("E       l valor de pi es: "+Math.PI);
        // metodo abs
        System.out.println("El valor absoluto de -26.7 es: "+Math.abs(-26.7));
        // metodo ceil
        System.out.println("El valor de 7.4 redondeado hacia arriba es: "+Math.ceil(7.4));
        // metodo floor
        System.out.println("El valor de 7.4 redondeado hacia abajo es: "+Math.floor(7.4));
        // metodo max   
        System.out.println("El valor maximo entre 8.6 y 5.2 es: "+Math.max(8.6,5.2));
        // metodo min
        System.out.println("El valor minimo entre 8.6 y 5.2 es: "+Math.min(8.6,5.2));
        // metodo pow   
        System.out.println("El valor de 5 elevado a la potencia de 3 es: "+Math.pow(5,3));
        // metodo sqrt
        System.out.println("La raiz cuadrada de 25 es: "+Math.sqrt(25));
        // metodo random
        System.out.println("Un numero aleatorio entre 0 y 1 es: "+Math.random());
    }
}
